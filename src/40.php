<?php
// This is a PHP script to demonstrate how to handle arrays and strings

$fruits = array("apple", "banana", "cherry");
echo "The first fruit is: " . $fruits[0]; // Output: The first fruit is: apple
echo "The last fruit in the array is: " . end($fruits); // Output: The last fruit in the array is: cherry

// Define a string with some characters
$string = "Hello, World!";
echo strlen($string); // Output: 13 (length of the string)
